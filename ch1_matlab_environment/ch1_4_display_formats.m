% 1.4 Display Formats

% This command can help you remember the formats
help format

% When you change the format, all following numbers will be displayed in
% that format

pi

format long
long=pi

format short
short=pi

format short e
shorte=pi

format long e
longe=pi

format hex
hex=pi

format bank
bank=pi

format + % displays whether a number is negative or positive
plus=pi

format rational % displays the number as a ratio of two whole numbers
rational=pi